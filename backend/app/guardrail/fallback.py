"""多级兜底策略。

层级：
1. LLM 瞬时失败 → 自动重试（见 llm.client 的 @_retry）。
2. 单个 Agent 异常 → 在编排中被 try/except 隔离，不影响整体。
3. RAG 无命中 → doc_rag 返回不确定提示并建议转人工。
4. 意图低置信 / 缺关键信息 → 进入澄清追问。
5. 工具超时/失败 → registry 返回降级结果，Agent 据此提示证据不足并可建单。
6. 用户要求人工 / 高风险操作 → 创建工单转人工。
7. 整个编排不可恢复异常 → 规则兜底回复 + 自动建单（本模块）。

本模块职责：
  - 定义当整个 Agent 编排链发生不可恢复异常时的最后一道防线。
  - 按意图类型提供预设的规则兜底话术（RULE_REPLIES），确保用户始终收到有意义的回复。
  - rule_reply() 函数供编排层 except 块调用，根据已识别的用户意图返回对应话术。
"""

# 不可恢复失败时的规则兜底话术（按意图）
# 键：意图标识符（与意图识别模块的输出对应）；值：对应的兜底回复文案
# 设计为字典而非 if/else，便于后续新增意图类型而无需修改函数逻辑
RULE_REPLIES = {
    # api_error：用户反馈 API 调用报错（如 400/500 等），诊断服务不可用时的兜底话术
    "api_error": "抱歉，诊断服务暂时不可用。我已为你登记问题并转交人工技术支持，请稍候。",
    # rate_limit：用户遇到限流（429），提供简要处理建议同时转人工兜底
    "rate_limit": "抱歉，诊断服务暂时不可用。常见的 429 处理是本地限速 + 指数退避重试，必要时升级套餐。我已转交人工进一步核查。",
    # billing：用户咨询账单问题，账单服务不可用时的兜底话术
    "billing": "抱歉，账单服务暂时不可用。我已登记你的账单问题并转交人工处理。",
    # data_quality：用户反馈数据质量问题，处理服务不可用时的兜底话术
    "data_quality": "抱歉，处理服务暂时不可用。我已登记你的数据质量问题并转交人工核查。",
    # doc_qa：用户进行文档问答，问答服务不可用时的兜底话术（建议重试或转人工）
    "doc_qa": "抱歉，问答服务暂时不可用，请稍后重试，或转人工技术支持。",
    # ticket：用户主动要求转人工或创建工单，直接告知已受理
    "ticket": "已收到你的请求，正在为你转接人工技术支持。",
    # chitchat：闲聊类意图，服务不可用时的轻量兜底（不建单，语气较轻松）
    "chitchat": "抱歉，我现在有点忙，请稍后再试。",
}

# 默认兜底话术：当意图未识别或意图不在 RULE_REPLIES 中时使用
# 措辞保持通用且明确告知已登记转人工，避免用户感到被忽视
DEFAULT_RULE_REPLY = "抱歉，服务暂时不可用。我已登记你的问题并转交人工技术支持，请稍候。"


def rule_reply(intent: str | None) -> str:
    """根据意图返回对应的规则兜底话术。

    参数：
        intent: 用户意图标识符（如 'api_error'、'billing'），
                为 None 时视为意图未知，使用默认兜底话术。

    返回：
        对应意图的兜底回复字符串；意图未匹配时返回 DEFAULT_RULE_REPLY。

    用途：
        在编排层的最外层 except 块中调用，确保任何不可恢复异常下
        用户都能收到一条有意义的回复，而不是看到系统错误堆栈。
    """
    # get() 查找意图对应话术，intent 为 None 时 key 为空字符串，同样走 default 路径
    return RULE_REPLIES.get(intent or "", DEFAULT_RULE_REPLY)
